void main() {
  int sh1 = 19;
  double sh2 = 11;
  var sh3 = sh1 + sh2;
  print(sh3);
}

/*
Explanation 
The program starts at main().

We create a variable called sh1 (which is an integer type) and give it the value 19.

We create another variable called sh2 (which is a double type) and give it the value 11.0.

Then we add sh1 and sh2 (19 + 11.0), and store the result (30.0) in a new variable called sh3. (sh3 is automatically decided as a decimal number)
Finally, we show the value of sh3, which is 30.0.



